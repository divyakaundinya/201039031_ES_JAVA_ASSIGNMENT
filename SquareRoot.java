import java.lang.Math;
  
class SquareRoot {
  
    public static void main(String args[])
    {
        double a = 30;
  
        System.out.println(Math.sqrt(a));
  
        a = 45;
  
        System.out.println(Math.sqrt(a));
  
        a = 60;
  
        System.out.println(Math.sqrt(a));
  
        a = 9;
  
        System.out.println(Math.sqrt(a));
    }
}